def generate_vocabulary_json(words: List[Tuple[str, str, str, int, int]]) -> Dict[str, Any]:
    """Generate JSON vocabulary file"""
    vocab_list = []

    for word, pos, meaning, difficulty, frequency in words:
        word_id = str(uuid.uuid4())

        # Simplified distractors
        distractors = ["異なる意味", "関連する概念", "正反対の意味"]

        choices = [
            {"id": f"{word_id}_0", "meaning": meaning, "isCorrect": True}
        ]

        for i, distractor in enumerate(distractors):
            choices.append({
                "id": f"{word_id}_{i+1}",
                "meaning": distractor,
                "isCorrect": False
            })

        random.shuffle(choices)

        vocab_list.append({
            "id": word_id,
            "word": word,
            "reading": "",
            "partOfSpeech": pos,
            "meaningJP": meaning,
            "exampleSentence": f"This is {word}.",
            "exampleTranslation": f"これは{meaning}です。",
            "difficulty": difficulty,
            "frequency": frequency,
            "eikenLevel": "pre_2nd",
            "category": get_pos_category(pos),
            "choices": choices
        })

    return {
        "vocabulary": vocab_list,
        "metadata": {
            "total_words": len(vocab_list),
            "level": "pre_2nd",
            "generated_date": "2026-03-20"
        }
    }

def get_pos_category(pos: str) -> str:
    """Convert POS to Japanese category"""
    mapping = {
        "noun": "名詞",
        "verb": "動詞",
        "adjective": "形容詞",
        "adverb": "副詞",
        "preposition": "前置詞",
        "conjunction": "接続詞",
        "interjection": "感動詞",
        "determiner": "限定詞",
        "pronoun": "代名詞"
    }
    return mapping.get(pos, "その他")

